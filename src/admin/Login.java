    //username validation code 
    private void unameTxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_unameTxtKeyReleased
        // TODO add your handling code here:
        //Nirala8084
        String PATTERN = "^[a-zA-Z0-9 ]{0,30}";
        Pattern patt = Pattern.compile(PATTERN);
        Matcher match = patt.matcher(unameTxt.getText().trim());
        if (!match.matches()) {
            JOptionPane.showMessageDialog(null, "Please Enter correct username");
            unameTxt.setText("");
        }
    }//GEN-LAST:event_unameTxtKeyReleased

