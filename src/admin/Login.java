    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:
        String name = unameTxt.getText().trim();
        String pass = new String(passTxt.getPassword()).trim();
        try {
            //Class.forName("com.mysql.jdbc.Driver");

            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/adhaven", "root", "1234");
            String qry;
            if (!typeCkbox.isSelected()) {
                qry = "SELECT *FROM user where name=? and password=? and type='user'";

                PreparedStatement pr = conn.prepareStatement(qry);
                pr.setString(1, name);
                pr.setString(2, pass);
                ResultSet rst = pr.executeQuery();
                if (rst.next()) {
                    new UserMain().setVisible(true);
                    this.setVisible(false);
                }
            }
            if (typeCkbox.isSelected()) {
                qry = "SELECT *FROM user where name=? and password=? and type='admin'";
                PreparedStatement pr = conn.prepareStatement(qry);
                pr.setString(1, name);
                pr.setString(2, pass);
                ResultSet rst = pr.executeQuery();
                if (rst.next()) {
                    new AdminMain().setVisible(true);
                    this.setVisible(false);
                }
            }
            conn.close();
        } catch (Exception e) {

            e.printStackTrace();
        }

    }//GEN-LAST:event_loginBtnActionPerformed

